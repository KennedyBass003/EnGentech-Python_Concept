def add_num(num1, num2):
    print(num1 + num2)


def sub_num(num1, num2):
    print(num1 - num2)


def mult_num(num1, num2):
    print(num1 * num2)


def div_num(num1, num2):
    print(num1 / num2)


def rem_num(num1, num2):
    print(num1 % num2)


add_num(2, 4)
sub_num(7, 3)
mult_num(2, 9)
div_num(64, 8)
rem_num(5, 2)
